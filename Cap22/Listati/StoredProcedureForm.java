    private void cb_selectTableActionPerformed(java.awt.event.ActionEvent evt)                                               
    {                                                   
        // ottengo quale item Ã¨ stato selezionato, il suo valore
        // e costruisco la query
        JComboBox cb = (JComboBox) evt.getSource();
        String s_procedure = (String) cb.getSelectedItem();
        int ix = cb.getSelectedIndex();
        
        ta_sdef.setText(procedures_def[ix]);
        
        // ottengo un oggetto CallableStatement
        String procedure = "{CALL " + s_procedure + "(?, ?)}";
        cs = DBHelper_REV_4.getCallableStatement(procedure, false);

        // variabili che conterranno i valori ritornati dalla procedura
        String us = "none";
        Date t = new Date();

        try
        {
            // registro il parametro di tipo OUT
            cs.registerOutParameter(1, java.sql.Types.VARCHAR);

            // registro il parametro di tipo INOUT
            cs.setDate(2, new java.sql.Date(t.getTime()));
            cs.registerOutParameter(2, java.sql.Types.DATE);
            
            cs.execute(); // eseguo la store

            // utente e ora
            us = cs.getString(1);
            t =  new Date(cs.getTimestamp(2).getTime());

            ta_result.setText(us + "\n" + t);
        }
        catch (SQLException ex) { ta_result.setText(ex.getMessage()); }
    }                                              

